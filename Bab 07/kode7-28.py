# angka
a = 123

# menampilkan integer
print("Angkanya: {:d}".format(a))

# menampilkan float
print("Bentuk floatnya: {:f}".format(a))

# menampilkan biner, octal, dan hex
print("Bentuk binernya: {:b}".format(a))
print("Bentuk octalnya : {:o}".format(a))
print("Bentuk hexnya: {:x}".format(a))
print("Bentuk karakternya: {:c}".format(a))
