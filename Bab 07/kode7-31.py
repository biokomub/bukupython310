# mengatur padding pada float dengan desimal yang diatur
# desimal diatur presisinya,
# dari presisi default (6) hingga kurang presisi (0) 
# Catatan: (.f) equivalen dengan (.6f)
print("{:5f}".format(12345))
print("{:5.6f}".format(12345))
print("{:5.5f}".format(12345))
print("{:5.4f}".format(12345))
print("{:5.3f}".format(12345))
print("{:5.2f}".format(12345))
print("{:5.1f}".format(12345))
print("{:5.0f}".format(12345))
