#mencetak angka pada string berpola dengan %-formatting

angka = 90 

#cetak string berpola
print("Ini angka %s" % angka)
print("%s adalah string dari angka itu."% angka)
print("%i adalah integer dari angka itu." % angka)
print("%f adalah float dari angka itu." % angka)
print("%o adalah octal dari angka itu." % angka)
print("%x adalah octal dari angka itu." % angka)
