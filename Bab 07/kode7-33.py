# string padding dengan rata kiri
print("{:<11}".format("Biologi"))

# string padding dengan rata kiri
# dan diapitkan dengan karakter "*"
print("{:*<11}".format("Biologi"))

# string padding dengan rata kanan
print("{:>11}".format("Biologi"))

# string padding dengan rata kanan
# dan diapitkan dengan karakter "*"
print("{:*>11}".format("Biologi"))

# string padding dengan rata tengah
print("{:^11}".format("Biologi"))

# string padding dengan rata tengah
# dan diapitkan dengan karakter "*"
print("{:*^11}".format("Biologi"))
