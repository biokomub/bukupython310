# tampilkan tanda sesuai angka
print("{:=f} {:=f}".format(45.23, -67.89))

# tampilkan tanda + bila bertemu angka positif
print("{:+f} {:+f}".format(45.23, -67.89))

# tampilkan tanda - bila bertemu angka negatif
print("{:-f} {:-f}".format(45.23, -67.89))

# tampilkan spasi bila bertemu angka positif
print("{: f} {: f}".format(45.23, -67.89))
