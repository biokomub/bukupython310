# mengatur padding pada string 
print("{:5}".format("Angka"))

# mengatur padding pada integer
print("{:5d}".format(1))
print("{:5d}".format(12))
print("{:5d}".format(123))
print("{:5d}".format(1234))
print("{:5d}".format(12345))

# mengatur padding pada float
print("{:5f}".format(1))
print("{:5f}".format(12))
print("{:5f}".format(123))
print("{:5f}".format(1234))
print("{:5f}".format(12345))
